cs = {
    " ": (0, 0, 0),
    "w": (255, 255, 255),
    "r": (255, 0, 0),
    "g": (0, 255, 0),
    "b": (0, 0, 255),
    "y": (255, 255, 0),
    "a": (0, 255, 255),
    "f": (255, 0, 255),
    "s": (192, 192, 192),
    "m": (128, 0, 0),
    "o": (255, 165, 0),
    "i": (75, 0, 130),
    "e": (80, 200, 120),
    "u": (18, 10, 143),
    "h": (165, 42, 42),
    "p": (255, 192, 203),
    "c": (220, 20, 60),
}
n = int(input())
res = []
for _ in range(n):
    x, y, z = (int(i) for i in input().split())
    c, d0 = " ", 3 * 255
    for i, v in cs.items():
        d = abs(v[0] - x) + abs(v[1] - y) + abs(v[2] - z)
        if d < d0:
            c, d0 = i, d
    res.append(c)
res = "".join(res)
print(res)
